package Day03.Examples.saikiran;

public class UnaryOperators {
    public static void main(String args[]){
        int i=5;
        i++;
        System.out.println(i);
        ++i;
        System.out.println(i);
        System.out.println(++i);//pre increment
        System.out.println(i++);//post increment
        System.out.println(i);

    }
}

